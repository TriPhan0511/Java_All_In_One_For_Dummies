/* Final classes */

/*
 * A final class is a class that can't be used as a base class. To declare a class as final,
 * just add the final keyword to the class declaration.
 * 
 * When you declare a class to be final, all of its methods are to be considered to be the
 * final as well. That makes sense when you thinking about it. Because you can't use a final
 * class as the base class for another class, no class can possibly in a position to override
 * any of the methods in the final class. Thus all the methods of a final class are final methods.
 * 
 * */


