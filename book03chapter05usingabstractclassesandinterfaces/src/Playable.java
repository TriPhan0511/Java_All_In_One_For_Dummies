
/**
 * Creating A Basic Interface
 * 
 * Here's a basic inteface that defines a single method, named Playable, that
 * includes a single method named play.
 * 
 * This interface declares that any class that implements the Playable interface must
 * provide an implementation for a method named play that accepts no parameters and
 * doesn't return a value.
 */

public interface Playable 
{
	void play();
}
